import { v } from "convex/values";
import { mutation, query } from "./_generated/server";

//
// LIST MESSAGES
//
export const list = query({
  args: { chatId: v.id("chats") },
  handler: async (ctx, { chatId }) => {
    return await ctx.db
      .query("messages")
      .withIndex("by_chat", (q) => q.eq("chatId", chatId))
      .order("asc")
      .collect();
  }
});

